package org.legion.steel.steelnet

import org.legion.steel.steelnet.config.CacheConfiguration
import org.legion.steel.steelnet.config.GoogleConfiguration
import org.legion.steel.steelnet.config.InfoConfiguration
import org.legion.steel.steelnet.dto.AliveStats
import org.springframework.boot.autoconfigure.SpringBootApplication
import org.springframework.boot.autoconfigure.security.servlet.SecurityAutoConfiguration
import org.springframework.boot.context.properties.EnableConfigurationProperties
import org.springframework.boot.runApplication
import org.springframework.cache.annotation.EnableCaching
import org.springframework.data.mongodb.repository.config.EnableMongoRepositories
import org.springframework.web.servlet.config.annotation.EnableWebMvc

@SpringBootApplication(exclude = [SecurityAutoConfiguration::class])
@EnableConfigurationProperties(GoogleConfiguration::class, CacheConfiguration::class, InfoConfiguration::class)
@EnableMongoRepositories
@EnableCaching
@EnableWebMvc
class SteelNetApplication

fun main(args: Array<String>) {
    AliveStats.startupTime = System.currentTimeMillis()
    runApplication<SteelNetApplication>(*args)
}
